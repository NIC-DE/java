package multiples6;


//Εισάγουμε την κλάση Scanner ώστε να μπορούμε να εισάγουμε αριθμούς ή Strings
import java.util.Scanner;

/**
 *
 * @author Papatheodorou Konstantinos
 */
public class Multiples6 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        // Ορίζουμε την μεταβλητή του αριθμού που θα δώσουμε 
        int number;
        
        // Εισάγουμε τον αριθμό
        Scanner s = new Scanner(System.in);
        System.out.println("Παρακαλώ δώστε έναν αριθμό για να υπολογίσω πόσα πολαπλάσια του 6 έχει :");
        number = s.nextInt();
        
        // Ορίζουμε την μεταβλητή i με την τιμή 6
        int i = 6;
        
        // Μηδενίζουμε τον μετρητή
        int counter = 0;
        
        
        // Τυπώνουμε τα πολλαπλάσια του 6
        System.out.println("Ο αρθμός που μου δώσατε έχει τα εξής πολλαπλάσια του 6:");
        
        
        
        /*Αν εισάγεις τον αριθμό `20`, ο βρόχος `while` εκτελείται με τα εξής βήματα, καθώς τυπώνει τα πολλαπλάσια του 6 που είναι μικρότερα ή ίσα με τον αριθμό αυτό.

Ο βρόχος `while` θα συνεχίζει να τρέχει όσο το `i` είναι μικρότερο ή ίσο του `20`. Ας δούμε αναλυτικά τα βήματα:

### Ανάλυση του βρόχου `while` για `number = 20`
1. **Αρχικές Τιμές**:
   - `i = 6` (το πρώτο πολλαπλάσιο του 6)
   - `counter = 0` (μετρητής πολλαπλασίων)

2. **Βρόχος `while`**:
   - **1η επανάληψη**:
     - Το `i` είναι `6`, το οποίο είναι μικρότερο ή ίσο του `20`.
     - Εκτυπώνει το `6`.
     - Αυξάνει το `i` κατά `6` -> τώρα `i = 12`.
     - Αυξάνει τον `counter` κατά `1` -> τώρα `counter = 1`.
     
   - **2η επανάληψη**:
     - Το `i` είναι `12`, το οποίο είναι μικρότερο ή ίσο του `20`.
     - Εκτυπώνει το `12`.
     - Αυξάνει το `i` κατά `6` -> τώρα `i = 18`.
     - Αυξάνει τον `counter` κατά `1` -> τώρα `counter = 2`.

   - **3η επανάληψη**:
     - Το `i` είναι `18`, το οποίο είναι μικρότερο ή ίσο του `20`.
     - Εκτυπώνει το `18`.
     - Αυξάνει το `i` κατά `6` -> τώρα `i = 24`.
     - Αυξάνει τον `counter` κατά `1` -> τώρα `counter = 3`.

   - **4η επανάληψη**:
     - Το `i` είναι `24`, το οποίο είναι μεγαλύτερο από το `20`, οπότε ο βρόχος σταματά.

3. **Αποτέλεσμα**:
   - Τυπώνει τα πολλαπλάσια του `6` που είναι μικρότερα ή ίσα με το `20`: `6`, `12`, και `18`.
   - Τυπώνει το συνολικό πλήθος πολλαπλασίων του `6` κάτω από το `20`, που είναι `3`.

### Έξοδος Προγράμματος για `number = 20`:
```
Παρακαλώ δώστε έναν αριθμό για να υπολογίσω πόσα πολαπλάσια του 6 έχει :
Ο αρθμός που μου δώσατε έχει τα εξής πολλαπλάσια του 6:
  6| 12| 18|
Σύνολο πολλαπλάσιων του 6 = 3
```
*/

        
        while (i <= number) {
            System.out.format("%3d|", i);
            i = i + 6;
            // Μετράμε πόσα 6 έχει ο αριθμός που μας έδωσαν
            counter = counter + 1;
        }
        
        // Αφήνουμε ένα κενό
        System.out.println();
        
        // Τυπώνουμε πόσα πολλαπλάσια του 6 έχει ο αριθμός που μας έδωσαν
        System.out.println("Σύνολο πολλαπλάσιων του 6 = " + counter);
      
    }

}
